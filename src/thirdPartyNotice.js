const fs = require("node:fs");
const path = require("node:path");

const SOURCE_URL = "https://github.com/VoltAgent/awesome-claude-code-subagents";
const SOURCE_LICENSE = "MIT License, (c) VoltAgent";

/**
 * MIT requires keeping the copyright/license notice with any copy — this keeps it
 * next to the agent files generated from template-agents/, whichever generator wrote them.
 */
function recordThirdPartyNotice(root, createdFilenames) {
  if (createdFilenames.length === 0) return null;
  const noticePath = path.join(root, ".claude", "agents", "THIRD_PARTY_NOTICES.md");
  const existing = fs.existsSync(noticePath) ? fs.readFileSync(noticePath, "utf8") : "";
  const header =
    "# Third-party subagent templates\n\n" +
    `The files below were generated by arthur-inspector from [${SOURCE_URL}](${SOURCE_URL}) (${SOURCE_LICENSE}). ` +
    "The MIT License requires keeping this notice alongside any copy of the code.\n\n";
  const newLines = createdFilenames.filter((f) => !existing.includes(f)).map((f) => `- \`${f}\``);
  if (newLines.length === 0) return noticePath;
  const body = (existing || header) + newLines.join("\n") + "\n";
  fs.writeFileSync(noticePath, body);
  return noticePath;
}

module.exports = { recordThirdPartyNotice, SOURCE_URL, SOURCE_LICENSE };
